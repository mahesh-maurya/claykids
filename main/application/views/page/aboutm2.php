<div id="groom">
    <h3 style="background: rgba(255, 255, 255, 0.88);padding: 7px;border-radius: 2px;color: rgb(255, 127, 2);">Grooming Program for Kids</h3>

<p>
    Ask any parent about their child's development, and they will often talk about speech, language, academics and physical growth.
</p>
<p>
    Is that all that is needed for an individual to excel in life?
</p>
<p>
    We at Clay believe that there is a lot more that is needed for the holistic development of a child. Social and Emotional learning is as important as
    academic learning. It helps the child to develop their personality which makes them unique.
</p>
<p>
    Developing good social skills is required for them to be able to ultimately form healthy relationships and adapt to various social situations easily. It
    goes a long way to help an individual in their social and corporate life.
</p>
<p>
    <h3>Structure of the program:</h3>
</p>
<p>
    The program is spread over a period of one year divided into four levels. Each level comprises of three months with a weekly class of 2 hours.
</p>

<p>
    <h3>Methodology:</h3>
</p>
<p>
    We all know that the attention span of children is limited. If we do not speak and make things interesting for them they are surely going to disconnect.
    Through play, activities and reflection, Clay helps child to know the complex learnings of life with ease.
</p>
<p>
    We encourage children to reason out what they learn. We give them logic behind everything which helps them to decide what is right and wrong. The program
    is conducted in a way which allows them to speak their mind, form opinions, learn from their mistakes and come out of their shell. The environment at clay
    gives them plenty of opportunity to express themselves openly and without being judgmental. There is nothing more satisfying and fulfilling for them to be
    able to express and being heard. We also have trained counsellor who identify the baseline behaviour of the child, does the root cause analysis and thus
    help us in modifying the behaviour and bring about a positive change.
</p>
<p>
